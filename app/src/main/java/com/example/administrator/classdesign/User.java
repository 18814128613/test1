package com.example.administrator.classdesign;

public class User {
	private String studentId;
	private String username;
	private String gradeYear;
	private String profession;
	private String idCard;
	private String email;
	private String headTeacher;
	private String assistant;
	/**
	 * @return studentId
	 */
	public String getStudentId() {
		return studentId;
	}
	/**
	 * @param studentId 要设置的 studentId
	 */
	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}
	/**
	 * @return username
	 */
	public String getUsername() {
		return username;
	}
	/**
	 * @param username 要设置的 username
	 */
	public void setUsername(String username) {
		this.username = username;
	}
	/**
	 * @return gradeYear
	 */
	public String getGradeYear() {
		return gradeYear;
	}
	/**
	 * @param gradeYear 要设置的 gradeYear
	 */
	public void setGradeYear(String gradeYear) {
		this.gradeYear = gradeYear;
	}
	/**
	 * @return profession
	 */
	public String getProfession() {
		return profession;
	}
	/**
	 * @param profession 要设置的 profession
	 */
	public void setProfession(String profession) {
		this.profession = profession;
	}
	/**
	 * @return idCard
	 */
	public String getIdCard() {
		return idCard;
	}
	/**
	 * @param idCard 要设置的 idCard
	 */
	public void setIdCard(String idCard) {
		this.idCard = idCard;
	}
	/**
	 * @return email
	 */
	public String getEmail() {
		return email;
	}
	/**
	 * @param email 要设置的 email
	 */
	public void setEmail(String email) {
		this.email = email;
	}
	/**
	 * @return headTeacher
	 */
	public String getHeadTeacher() {
		return headTeacher;
	}
	/**
	 * @param headTeacher 要设置的 headTeacher
	 */
	public void setHeadTeacher(String headTeacher) {
		this.headTeacher = headTeacher;
	}
	/**
	 * @return assistant
	 */
	public String getAssistant() {
		return assistant;
	}
	/**
	 * @param assistant 要设置的 assistant
	 */
	public void setAssistant(String assistant) {
		this.assistant = assistant;
	}
}